nome = input("Escreva seu nome: ")
print(nome)

#Todas maiusculas

nome = nome.upper()
print(nome)
#todas minusculas

nome = nome.lower()
print(nome)

#primeira letra maiuscula
nome = nome.capitalize()
print(nome)

#primeiras letras maiusculas
nome = nome.title()
print(nome)

#separar o texto e imprimir a posição 02
nome = nome.split()[1]
print(nome)

